	<p>Öntevékeny és tettre kész emberekben fogalmazódott meg a gondolat: „Az emberi élet és a környezetünk védelme mindannyiunk közös ügye, és ezért tenni is muszáj!” okán szükség van egy olyan civil szervezetre, amely az egyszerű szerveződésen túlmutatóan megfelelő keretben végzi emberbaráti feladatát. Ez a nemes gondolat vezetett a Bács-Kiskun Megyei Polgári Védelmi Szövetség megalakításához (2002).
	<p>A Szövetség az alapítók akaratának megfelelően a Magyar Polgári Védelmi Szövetség tagszervezete.</p>
	<p>A Szövetség tevékenységével aktívan közreműködik a megye polgárainak polgári védelmi és katasztrófavédelmi felkészítésében, védelmük szervezésében. Szervezetünk elhatárolódik minden politikai tevékenységtől.</p>
	<p>A Szövetség legfőbb célkitűzése, egy olyan az egész megyét átfogó tanácsadó - felkészítő rendszer megszervezése és működtetése, melynek eredményeként az állampolgárok megismerhetik környezetük veszélyforrásait, képessé válnak önmaguk és embertársaik életét, vagyontárgyait eredményesen oltalmazni, menteni, mind az elemi csapások, mind az ipari katasztrófák esetén. A célkitűzéseink megvalósítását csoportjainkon keresztül érjük el, melynek élén a koordinátorok állnak.</p>
	<p>A fenti feladatok érdekében évekkel a 234/2011 (XI.10.) Kormányrendelet által létrehozott közbiztonsági referens intézményének bevezetése előtt végrehajtottuk a polgármester segítők felkészítését. Ők azok, akik a polgármesterek munkáját közvetlenül segítették és támogatták a polgári védelmi és katasztrófavédelmi feladatainak végzésében. A közbiztonsági referensek ugyan ezt a munkát végzik, azzal a kiegészítéssel, hogy rendvédelmi és honvédelmi területen is segítik a polgármester munkáját. A közbiztonsági referensek képzése folyamatos, ezt a munkát a Bács-Kiskun Megyei Katasztrófavédelmi Igazgatósággal közösen végezzük.</p>
	<p>De nem csak a vezetőket, hanem a lakosságot is el kell érnünk. Ennek érdekében – anyagi lehetőségünk függvényében - kiadványokat készítünk.</p>
	<p>Az ifjúságra, mint kiemelt célcsoportra tekintünk. Szintén a katasztrófavédelmi igazgatósággal közösen felkészítéseket és versenyeket rendezünk.  Az eredmények önmagukért beszélnek (lásd az Ifjúsági versenyek lapfület).</p>
	<p>Az ifjúság felkészítését nyári táboroztatás keretében is végezzük. Széles körű nemzetközi kapcsolatainkat kihasználva hol belföldön, hol a környező országokban tesszük emlékezetessé a velünk tartó általános- és középiskolások élményeit.</p>
	<p>2015 óta egy új feladatrendszerrel bővült a tennivalónk. Nevezetesen a megyei-, járási- és települési önkéntes mentőcsoportok képzéséhez, felszerelésük fejlesztéséhez és bővítéséhez, valamint a működésükhöz való hozzájárulás.</p>
	<p>Tagjaink a katasztrófák vagy a katasztrófaveszélyes helyzetek elhárításában közvetlenül is részt vettek. E tekintetben kiemelkedik a tiszakécskei és a bajai árvízi védekezés.</p>
	<p>Munkánk nem öncélú. Talán ezt a tényt az is mutatja, hogy számos támogatónk (magánemberek, önkormányzatok, gazdálkodó szervezetek) van, akik azonosulnak céljainkkal, és hozzásegítenek eredményes működésünkhöz. Amennyiben teheti, kérem Ön is segítsen és csatlakozzon hozzánk.</p>
